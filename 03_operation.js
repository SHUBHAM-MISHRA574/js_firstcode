//in case of two values the output always be string
console.log("1"+2);//it will give a string  ie, 12.
console.log(1+"2");//it will also give us string ie,12.
//in case of three outputs lets see
console.log("1"+2+3);//it will give string ie,123.
console.log(1+"2"+3);//it will also give a string value ie,123.
console.log(1+2+"3");//it will give a string value but different from the above cases here it will add the first two number and then convert it into string ie,33





